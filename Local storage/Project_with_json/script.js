function AddToCart(items){

 let found = false;
  for (let i = 0; i < CartList.length; i++) {
    if (CartList[i].id === items.id) {
      CartList[i].Qty += 1; // increase quantity
      found = true;
      break;
    }
  }

  if (!found) {
    CartList.push(items); // new item with Qty 1
  }
 localStorage.setItem("Cartstore",JSON.stringify(CartList));
    CartDisplay();
}

